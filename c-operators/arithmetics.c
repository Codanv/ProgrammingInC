#include <stdio.h>

int main()
{

  int num1 = 10, num2 = 2, result1, result2;

  // Arithmetic operators
  result1 = num1 / num2; // quotient
  result2 = num1 % num2; // remainder

  printf("Result1: %d \n", result1);
  printf("Result2: %d \n", result2);
  printf("5/2.0 : %d \n", (5 / 2));

  return 0;
}